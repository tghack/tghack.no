<div class="mx-auto max-w-6xl py-22 px-8">
    <p class="mb-4 text-body text-4xl font-bold tracking-tight text-heading md:text-5xl lg:text-6xl">
        Hva er en CTF
    </p>

    <div class="mb-5 text-lg">
        CTF er en forkortelse for Capture The Flag, og er en type hackingkonkurranse. De mest kjente typene er Jeopardy-style CTF og attack-defence CTF. <br>
        På TG:HACK arrangerer vi en Jeopardy-style CTF. Som i Jeopardy finnes det flere kategorier, og hver kategori har flere oppgaver med et visst antall poeng. <br>
        Poengene representerer vanligvis hvor vanskelig oppgaven er.
    </div>

    <div class="mb-5 text-lg">
        <h1 class="text-4xl font-bold">
            Hva er de forskjellige kategoriene?
        </h1>
        Det finnes flere kategorier i CTF-er. Her presenterer vi de mest vanlige kategoriene, og de vi bruker i konkurransen vår.
    </div>

    <div class="mb-5 text-lg">
        <h1 class="text-2xl font-bold">Crypto</h1>
        Crypto er en forkortelse for kryptografi, der informasjon beskyttes ved å krypteres (en form for transformasjon) til et uleselig format, også kalt chiffertekst. <br>
        Chifferteksten kan kun leses av de som har den hemmelige nøkkelen som trengs for å dekryptere den.
    </div>

    <div class="mb-5 text-lg">
        <h1 class="text-2xl font-bold">Web</h1>
        I web-oppgaver må du finne svakheter i ulike nettsider og tjenester. De fleste oppgavene løses i nettleseren. <br>
        Vanlige oppgaver inkluderer SQL-injeksjon, XSS og validering av input.
    </div>

    <div class="mb-5 text-lg">
        <h1 class="text-2xl font-bold">AFK</h1>
        AFK står for away from keyboard. Når en CTF arrangeres fysisk et sted, krever AFK-oppgaver som regel at man er til stede. <br>
        På TG:HACK må du ofte besøke TG:HACK-standen – og vi synes det er veldig hyggelig når dere kommer og prater med oss! ^_^)/*
    </div>

    <div class="mb-5 text-lg">
        <h1 class="text-2xl font-bold">Pwn</h1>
        Å “pwn’e” et system betyr å utnytte en sårbarhet for å få høyere rettigheter enn det som er ment. <br>
        I denne kategorien må du oppdage sårbarheter og utnytte dem for å få kjøre kode eksternt. Dette er ofte den vanskeligste kategorien.
    </div>

    <div class="mb-5 text-lg">
        <h1 class="text-2xl font-bold">Reversing</h1>
        Reversering (reverse engineering) er å plukke fra hverandre et system for å finne ut hvordan det fungerer.<br>
        I denne kategorien må du analysere programmer for å finne skjulte flagg og annen informasjon.
    </div>

    <div class="mb-5 text-lg">
        <h1 class="text-2xl font-bold">Forensics (og Stego)</h1>
        I forensics må du undersøke filer for å finne skjulte spor og meldinger. <br>
        Filene kan være nettverkstrafikk (packet captures) eller andre filer som bilder, zip-filer eller tekstfiler. Kategorien ligner mye på stego. <br>
        Stego er en forkortelse for steganografi, som handler om å skjule en hemmelig melding i noe annet, for eksempel i et bilde – som å gjemme en zip-fil inne i et bilde.
    </div>

    <div class="mb-5 text-lg">
        <h1 class="text-2xl font-bold">Misc</h1>
        Misc er en forkortelse for miscellaneous (diverse), og inneholder oppgaver som ikke passer inn i de andre kategoriene. <br>
        Noen ganger betyr dette at oppgaven dekker flere kategorier samtidig.
    </div>
</div>